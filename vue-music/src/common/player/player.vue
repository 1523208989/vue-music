        v-show="!fullScroll && playListUrl[index]"
        @click="full"
      >
        <div class="img_box">
          <img v-lazy="song.img_url" alt="" />
        </div>
        <div class="centent">
          <p>{{ song.title }}</p>
          <p>{{ song.name }}</p>
        </div>
        <i :class="`iconfont ${playClass}`" @click.stop="setPlay(!play)"></i>
        <i class="iconfont icongedan"></i>
      </div>
    </transition>
    <audio ref="audio" autoplay :src="audioUrl"></audio>
  </div>
</template>
<script>
import { mapState, mapMutations } from "vuex";
import animations from "create-keyframe-animation";
export default {
  mounted() {
    this.setPlay(true);
  },
  computed: {
    ...mapState([
      "fullScroll",
      "index",
      "playList",
      "playListUrl",
      "song",
      "play",
    ]),
    ...mapState({
      fullScroll: "fullScroll",
      index: "index",
      playList: "playList",
      song: "song",
      play: "play",
      playListUrl: "playListUrl",
    }),
    playClass() {
      return this.play ? "iconicon_bofang" : "iconbofang";
    },
    audioUrl() {
      return (
        this.playListUrl[this.index] &&
        `http://ws.stream.qqmusic.qq.com/${this.playListUrl[this.index]}`
      );
    },
  },
  methods: {
    ...mapMutations(["setFullScroll", "setPlay", "setPlayList"]),
    ...mapMutations({
      setFullScroll: "setFullScroll",
      setPlay: "setPlay",
      setPlayList: "setPlayList",
      setIndex: "setIndex",
    }),
    close() {
      this.setPlayList([]);
      this.setIndex(-1);
    },
    down() {
      this.setFullScroll(false);
    },
    full() {
      this.setFullScroll(true);
    },
    enter(el, done) {
      const { x, y, scale } = this.getPosition();
      animations.registerAnimation({
        name: "move",
        animation: {
          0: {
            transform: `translate3d(${x}px,${y}px,0) scale(${scale})`,
          },
          60: {
            transform: `translate3d(-50%,0,0) scale(1.1)`,
          },
          100: {
            transform: `translate3d(-50%,0,0) scale(1)`,
          },
        },
        presets: {
          duration: 800,
        },
      });
      animations.runAnimation(this.$refs.cd, "move", done);
    },
    afterEnter(el) {
      animations.unregisterAnimation("move");
      this.$refs.cd.style.animation = "";
    },
    leave(el, done) {
      const { x, y, scale } = this.getPosition();
      this.$refs.cd.style.transition = "all 0.3s";
      this.$refs.cd.style.transform = `translate3d(${x}px,${y}px,0) scale(${scale})`;
      this.$refs.cd.addEventListener("transitionend", done);
    },
    afterLeave(el) {
      this.$refs.cd.style.transition = "";
      this.$refs.cd.style.transform = "";
    },
    getPosition() {
      const min_x = window.innerWidth * 0.07 + 10;
      const min_y = window.innerHeight - 32.5;
      const full_x = window.innerWidth / 2 + window.innerWidth * 0.45;
      const full_y = 90 + window.innerWidth * 0.45;
      const x = min_x - full_x;
      const y = min_y - full_y;
      const scale = 14 / 90;
      return {
        x,
        y,
        scale,
      };
    },
  },
  watch: {
    song() {
      this.setPlay(true);
    },
    play(newV) {
        newV ? this.$refs.audio.play() : this.$refs.audio.pause();
    },
  },
};
</script>

